        "from __future__ import annotations #to use Optional[str] hints\n",
        "\n",
        "import re     #to extract UCI move from model text\n",
        "import random   #to never crash/return illegal\n",
        "from typing import Optional, List, Tuple\n",
        "\n",
        "import chess #to read FEN, generate legal moves etc..\n",
        "import torch\n",
        "from transformers import AutoTokenizer, AutoModelForCausalLM, BitsAndBytesConfig\n",
        "\n",
        "_UCI_RE = re.compile(r\"\\b([a-h][1-8][a-h][1-8][qrbn]?)\\b\", re.IGNORECASE) #allowed moves formation\n",
        "\n",
        "\n",
        "class TransformerPlayer(Player):\n",
        "\n",
        "   \"\"\"\n",
        "   Transformer-based chess player.\n",
        "    - Input: FEN string\n",
        "    - Output: UCI move string (e.g., 'e2e4', 'e7e8q') or None if no legal moves\n",
        "\n",
        "    Upgrade: candidate scoring (reranking) over legal moves for stronger, more stable play.\n",
        "   \"\"\"\n",
        "   def __init__(\n",
        "        self,\n",
        "        name: str,\n",
        "        model_name: str = \"Qwen/Qwen3.5-4B\",\n",
        "        seed: int = 123,  #ensure repeatble results\n",
        "        use_4bit: bool = True,  #use model in 4-bit to fit in Colab GPU memory\n",
        "        # generation fallback params\n",
        "        max_new_tokens: int = 8,\n",
        "        temperature: float = 0.2,\n",
        "        top_p: float = 0.9,\n",
        "        # candidate scoring params for strengthening machine\n",
        "        use_candidate_scoring: bool = True,     #if true, score a legal move instead of randomly generated\n",
        "        max_candidates: int = 48,     #score only top N legal moves\n",
        "        score_batch_size: int = 8,     #batch size for scoring\n",
        "    ):\n",
        "        super().__init__(name)\n",
        "\n",
        "        random.seed(seed)\n",
        "        self.model_name = model_name\n",
        "\n",
        "        # Backup generation settings (only used if scoring fails)\n",
        "        self.max_new_tokens = max_new_tokens\n",
        "        self.temperature = temperature\n",
        "        self.top_p = top_p\n",
        "\n",
        "        # Candidate scoring settings\n",
        "        self.use_candidate_scoring = use_candidate_scoring\n",
        "        self.max_candidates = max_candidates\n",
        "        self.score_batch_size = score_batch_size\n",
        "\n",
        "        # Tokenizer\n",
        "        self.tokenizer = AutoTokenizer.from_pretrained(\n",
        "            model_name,\n",
        "            use_fast=True,\n",
        "            trust_remote_code=True,\n",
        "        )\n",
        "\n",
        "        # (4-bit) to avoid syntax mismatch\n",
        "        quant_cfg = None\n",
        "        if use_4bit:\n",
        "            quant_cfg = BitsAndBytesConfig(\n",
        "                load_in_4bit=True,\n",
        "                bnb_4bit_quant_type=\"nf4\",\n",
        "                bnb_4bit_compute_dtype=torch.float16, #speed\n",
        "                bnb_4bit_use_double_quant=True,   #extra compression trick\n",
        "            )\n",
        "\n",
        "        # Load actual model\n",
        "        self.model = AutoModelForCausalLM.from_pretrained(\n",
        "            model_name,\n",
        "            device_map=\"auto\",\n",
        "            quantization_config=quant_cfg,\n",
        "            trust_remote_code=True,\n",
        "        )\n",
        "        self.model.eval()\n",
        "\n",
        "    # Utilities\n",
        "   def _legal_moves_uci(self, board: chess.Board) -> List[str]:\n",
        "        return [m.uci() for m in board.legal_moves]   # convert python-chess Move objects to UCI strings\n",
        "\n",
        "   def _extract_uci(self, text: str) -> Optional[str]:\n",
        "        text = text.strip().lower()\n",
        "        m = _UCI_RE.search(text)\n",
        "        return m.group(1).lower() if m else None\n",
        "\n",
        "\n",
        "    # Candidate scoring!\n",
        "   def _candidate_subset(self, board: chess.Board, legal_uci: List[str]) -> List[str]:\n",
        "        \"\"\"\n",
        "        We don't score ALL legal moves (could be 30-80). It's slow.\n",
        "        So we keep \"more important\" moves first:\n",
        "        promotions > checks > captures > castles > others\n",
        "        \"\"\"\n",
        "        moves = [chess.Move.from_uci(m) for m in legal_uci]\n",
        "\n",
        "        promos, captures, castles, others = [], [], [], []\n",
        "        for mv in moves:\n",
        "            if mv.promotion is not None:\n",
        "                promos.append(mv) #to ensure pawn promotion is taken into considertion\n",
        "            elif board.is_capture(mv):\n",
        "                captures.append(mv)   #critical captures\n",
        "            elif board.is_castling(mv):\n",
        "                castles.append(mv)    #castling for king safety\n",
        "            else:\n",
        "                others.append(mv)\n",
        "\n",
        "        checks = []   #this chunk to temporarily play the move and undo it if move gives check\n",
        "        for mv in moves:\n",
        "            board.push(mv)\n",
        "            if board.is_check():\n",
        "                checks.append(mv)\n",
        "            board.pop()\n",
        "\n",
        "        ranked = promos + checks + captures + castles #priority order\n",
        "        seen = set(ranked)\n",
        "        ranked += [m for m in others if m not in seen]\n",
        "\n",
        "        ranked_uci = [m.uci() for m in ranked]\n",
        "        if len(ranked_uci) > self.max_candidates:\n",
        "            ranked_uci = ranked_uci[: self.max_candidates]\n",
        "        return ranked_uci     #list of moves we will actually score\n",
        "\n",
        "   def _build_scoring_prompt(self, fen: str) -> str:\n",
        "        # Keep stable and short, this promt is the same for all candidates.\n",
        "        return (\n",
        "            \"You are a strong chess player.\\n\"\n",
        "            \"Given this position in FEN:\\n\"\n",
        "            f\"{fen}\\n\\n\"\n",
        "            \"Best move in UCI:\"\n",
        "        )\n",
        "\n",
        "   @torch.no_grad()\n",
        "   def _score_candidates(self, prompt: str, candidates: List[str]) -> List[float]:\n",
        "        \"\"\"\n",
        "        Score each candidate by mean log-prob of its tokens as continuation of the prompt.\n",
        "        The higher the score, the more model prefers that candidate.\n",
        "        \"\"\"\n",
        "        scores: List[float] = []\n",
        "\n",
        "        # Prompt length in tokens (candidate starts after this)\n",
        "        prompt_ids = self.tokenizer(prompt, return_tensors=\"pt\")[\"input_ids\"].to(self.model.device)\n",
        "        prompt_len = prompt_ids.shape[1]\n",
        " # Score candidates in batches (faster on GPU)\n",
        "        for i in range(0, len(candidates), self.score_batch_size):\n",
        "            batch = candidates[i : i + self.score_batch_size]\n",
        "            texts = [f\"{prompt} {c}\" for c in batch]  # prompt and after a candidate, since leading space helps tokenization\n",
        "\n",
        "            enc = self.tokenizer(texts, return_tensors=\"pt\", padding=True)\n",
        "            enc = {k: v.to(self.model.device) for k, v in enc.items()}\n",
